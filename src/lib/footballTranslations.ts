const norm = (s: string) => s.trim().replace(/\s+/g, ' ').toLowerCase();

export function translateTeamName(name?: string | null): string {
  if (!name) return name ?? '';
  const raw = String(name);
  return TEAM_NAME_MAP[norm(raw)] ?? raw;
}

export function translateTeamsInTitle(title?: string | null): string {
  if (!title) return title ?? '';
  const raw = String(title);
  const m = raw.match(/^(.*?)(\s+(?:x|vs\.?|×|@|-)\s+)(.*)$/i);
  if (m) {
    return `${translateTeamName(m[1])}${m[2]}${translateTeamName(m[3])}`;
  }
  return translateTeamName(raw);
}

export function translateCompetitionName(name?: string | null): string {
  if (!name) return name ?? '';
  const raw = String(name);
  const key = norm(raw);
  if (COMPETITION_NAME_MAP[key]) return COMPETITION_NAME_MAP[key];
  // Partial match: "UEFA Champions League 2024" → contains "uefa champions league"
  for (const [k, v] of Object.entries(COMPETITION_NAME_MAP)) {
    if (key.includes(k)) return raw.replace(new RegExp(k, 'i'), v);
  }
  return raw;
}
